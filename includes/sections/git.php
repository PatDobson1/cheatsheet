<h2>Git</h2>

<!-- Git basics --------------------------------------------------------------->
    <details>
        <summary>Git basics</summary>
        <div>
            <ul>
                <li>Get the current status <code class="inline-small">git status</code></li>
                <li>Add all files <code class="inline-small">git add .</code></li>
                <li>Add a specific file <code class="inline-small">git add .\includes\sections\git.php</code></li>
                <li>Commit staged files <code class="inline-small">git commit -m "commit message"</code></li>
                <li>Commit staged files <code class="inline-small">git commit -m "commit title" -m "commit message"</code> (with multiple commit messages - first is title)</li>
                <li>Push staged files <code class="inline-small">git push</code></li>
                <li>Pull latest from repo <code class="inline-small">git pull</code></li>
                <li>Clone a repo <code class="inline-small">git clone https://url-of-repo</code></li>
            </ul>
        </div>
    </details>
